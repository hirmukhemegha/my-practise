public class trycatch {
    public static void main(String[] args) {
        try {
            int a = 12;
            int b = 0;
            int c = a / b;
            System.out.println(c);
        } catch (Exception e) {
            System.out.println("error");
        }
        System.exit(0);
        System.out.println("hello mwgha");
    }

}
